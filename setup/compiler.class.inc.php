		}

		// Let's make the whole class declaration
		//
		$sClassName = $oClass->getAttribute('id');
		$bIsAbstractClass = ($oProperties->GetChildText('abstract') == 'true');
		$oPhpParent = $oClass->GetUniqueElement('php_parent', false);
		$aRequiredFiles = array();
